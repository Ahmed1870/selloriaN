export const getFomoMessage = (hoursLeft) => {
  if (hoursLeft > 24) {
    return "استمتع بمميزات الإمبراطور مجاناً! مبيعاتك زادت 30% النهاردة بفضل الذكاء الاصطناعي 🚀";
  }
  if (hoursLeft <= 24 && hoursLeft > 1) {
    return "⚠️ تحذير: فاضل أقل من يوم ومميزات 'رادار المنافسين' هتقفل.. متخليش غيرك يسبقك في فيصل! 🚦";
  }
  if (hoursLeft <= 1) {
    return "🚨 فرصة أخيرة: فاضل 60 دقيقة والـ AI هيتوقف عن الفصال مع زباينك.. جدد اشتراكك دلوقتي عشان مبيعاتك متوقفش! 💸";
  }
};
